let currentLine = 0;
let glowIntensity = 0;
let glowDirection = 1;

// Animate the glow effect
function animateGlow() {
    const asciiElement = document.getElementById('ascii-art');
    glowIntensity += glowDirection * 0.02;

    if (glowIntensity >= 1 || glowIntensity <= 0) {
        glowDirection *= -1;
    }

    const shadowIntensity = 10 + (glowIntensity * 20);
    asciiElement.style.textShadow = `0 0 ${shadowIntensity}px rgba(255, 215, 0, ${0.5 + glowIntensity * 0.5})`;
}

// Update text display
function updateText() {
    const lyricsElement = document.getElementById('lyrics-text');
    lyricsElement.classList.remove('pulse');

    // Trigger reflow to restart animation
    void lyricsElement.offsetWidth;

    lyricsElement.textContent = textLines[currentLine];
    lyricsElement.classList.add('pulse');
    currentLine = (currentLine + 1) % textLines.length;
}

// Initialize
document.getElementById('ascii-art').textContent = asciiArt;
updateText();

// Set intervals for animations
setInterval(animateGlow, 50);   // Smooth glow animation
setInterval(updateText, 4000);  // Text changes every 4 seconds
